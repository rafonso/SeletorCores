    private void formMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMousePressed
        if (evt.getClickCount() == 2 && evt.getButton() == MouseEvent.BUTTON3) {
            this.setCor(this.createRandomColor(), null);
        } else if (evt.getClickCount() == 2 && evt.getButton() == MouseEvent.BUTTON1) {
            JOptionPane.showMessageDialog(this, new ColorDescriptionPanel(this.getBackground()), "Valor da cor", JOptionPane.PLAIN_MESSAGE);
        }
        KeyboardFocusManager.getCurrentKeyboardFocusManager().clearGlobalFocusOwner();
    }//GEN-LAST:event_formMousePressed

