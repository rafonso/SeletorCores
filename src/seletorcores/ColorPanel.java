    private static String COLOR_FORMAT = "RGB(%03d, %03d, %03d, %03d) ";
    private Set<ColorChangedListener> components = new HashSet<ColorChangedListener>();
    Random random = new Random();

    /** Creates new form ColorPanel */
    public ColorPanel() {
        initComponents();
        // Init Colors
        this.setBackground(this.createRandomColor());
    }

    private Color createRandomColor() {
        return new Color(random.nextInt(256), random.nextInt(256), random.nextInt(256), 255);
    }

    private void showColor() {
        Color bkColor = this.getBackground();
        this.pnlCor.setBackground(new Color(255, 255, 255, bkColor.getAlpha()));
        this.lblCor.setText(String.format(COLOR_FORMAT, bkColor.getRed(), bkColor.getGreen(), bkColor.getBlue(), bkColor.getAlpha()));
    }

    public void setCor(Color color, ColorChangedListener source) {
        this.setBackground(color);
        showColor();
        for (ColorChangedListener colorChangedListener : components) {
            if (colorChangedListener != source) {
                colorChangedListener.colorChanged(color);
            }
        }
    }

    public boolean addColorChangedListener(ColorChangedListener component) {
        return this.components.add(component);
    }

    public void init() {
        this.setCor(this.getBackground(), null);
    }

